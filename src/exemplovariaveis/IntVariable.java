package src.exemplovariaveis;

public class IntVariable {
	public static void main(String[] args) {
		int idade = 34;
		System.out.println("Idade: " + idade + ".");
		
		idade *= 2;
		System.out.println("Idade multiplicada por 2: " + idade);
		
		idade /= 4;
		System.out.println("Idade dividida por 4: " + idade);
		
		idade += 5;
		System.out.println("Idade somada a 5: " + idade);
		
		idade -= 11;
		System.out.println("Idade diminuida a 11: " + idade);
		
	}
}
